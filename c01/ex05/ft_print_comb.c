#include <unistd.h>

void	ft_print_comb(void)
{
	char	n1[3];

	n1[0] = '0';
	while (n1[0] <= '7')
	{
		n1[1] = n1[0] + 1;
		while (n1[1] <= '8')
		{
			n1[2] = n1[1] + 1;
			while (n1[2] <= '9')
			{
				write (1, &n1[0], 1);
				write (1, &n1[1], 1);
				write (1, &n1[2], 1);
				if (!(n1[0] == '7' && n1[1] == '8' && n1[2] == '9'))
					write (1, ", ", 2);
				n1[2]++;
			}
			n1[2] = 0;
			n1[1]++;
		}
		n1[1] = 0;
		n1[0]++;
	}
}
