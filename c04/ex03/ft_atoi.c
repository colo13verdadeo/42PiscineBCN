int	ft_atoi(char *str)
{
	int	i1;
	int	res;

	i1 = 1;
	res = 0;
	while (*str)
	{
		if (*str == '\n' || *str == '\f' || *str == '\t'
			|| *str == '\r' || *str == '\v' || *str == ' ' || *str == '+');
		else if (*str == '-')
			i1 *= -1;
		else if (!(*str >= '0' && *str <= '9'))
		{
			return (res * i1);
		}
		else
			res = (10 * res) + (str[0] - '0');
		str++;
	}
	if (res == -2147483648)
		return (-2147483648);
	return (res * i1);
}
